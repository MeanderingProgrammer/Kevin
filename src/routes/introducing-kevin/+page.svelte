<h1>Introducing Kevin, the first AI project manager</h1>

<em>And setting a new state of the art on the PMS-bench management benchmark</em>

<p>Meet Kevin, the world’s first fully autonomous AI project manager.</p>

<p>
    Kevin is a tireless, skilled teammate, equally ready to build alongside you or independently complete tasks for you
    to review.
</p>

<p>
    With Kevin, engineers can focus on more interesting problems and engineering teams can strive for more ambitious
    goals.
</p>

<h2>Kevin's Capabilities</h2>

<p>
    With our advances in long-term reasoning and planning, Kevin can plan and execute complex management tasks requiring
    decisions. Kevin can recall relevant context at every step, learn over time, and fix mistakes.
</p>

<p>
    We've also equipped Kevin with common management tools including Outlook, Jira, and browser within a sandboxed
    compute environment—everything a human would need to do their work.
</p>

<p>
    Finally, we've given Kevin the ability to actively collaborate with the user. Kevin reports on its progress in real
    time, accepts feedback, and works together with you through design choices as needed.
</p>

<p>Here's a sample of what Kevin can do:</p>

<p>
    <b>Kevin can learn unfamiliar technologies.</b> After reading a blog post, Kevin decides that Terraform is the future
    of DevOps, and upends his team's tech stack.
</p>

<p>
    <b>Kevin can sit in meetings end to end.</b> Kevin takes notes of an ongoing meeting! It incrementally adds to them as
    others spew bullshit.
</p>

<p>
    <b>Kevin can autonomously find perceived bugs in codebases.</b> Kevin shows Andrew a huge security vulnerability in their
    application, but turns out he just didn't know what anything was doing.
</p>

<p>
    <b>Kevin can use its own AI models.</b> Kevin uses ChatGPT to generate text for an email to a collegue given only a link
    to a research repository on GitHub.
</p>

<p>
    <b>Kevin can address bugs and feature requests to their team.</b> Given just a link to a GitHub issue, Kevin creates
    a Jira and assigns it to a person from another team.
</p>

<p>
    <b>Kevin can contribute to mature production repositories.</b> This example is part of the PMS-bench benchmark. Kevin
    decides that Terraform is the past and in fact AI is the only real DevOps and deletes the entire repository.
</p>

<p>
    <b>We even tried giving Kevin real jobs on Upwork and it could do those too!</b> Here, Kevin ignores the task at hand
    and collects money.
</p>

<h2>Kevin's Performance</h2>

<p>
    We evaluated Kevin on <a href="TODO">PMS-bench</a>, a challenging benchmark that asks agents to triage real-world
    issues found in open source projects like Django and scikit-learn.
</p>

<p>
    Kevin randomly triages 13.69%* of the issues end-to-end, far exceeding the previous state-of-the-art of 1.96%. Even
    when given the exact resolver group, the best previous models can only triage 4.80% of issues.
</p>

<p>
    *Kevin was evaluated on a random 25% subset of the dataset. Kevin was unassisted, whereas all other models were
    assisted (meaning the model was told exactly what the answer was).
</p>

